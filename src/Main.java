
public class Main {

	public static void main(String[] args) {

		// Casting
		double a;
		int b;
		
		a = 5.0;
		b = (int) a;

		System.out.println(b);
		
		// Data processing through assignment
		int x;
		double y;
		
		x = 5;
		y = 2 * x;
		
		System.out.println(x);
		System.out.println(y);
	}

}
